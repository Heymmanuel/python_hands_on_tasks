num1 = int(input("Enter any number: "))
num2 = int(input("Enter any number: "))

addition = num1 + num2
print(f'Sum of {num1} and {num2} is {addition}')

difference = num1 - num2
print(f'Difference of {num1} and {num2} is {difference}')

product = num1 * num2
print(f'Product of {num1} and {num2} is {product}')

Quotient = num1 / num2
print(f'Quotient of {num1} and {num2} is {Quotient}')

modulus = num1 % num2
print(f'Modulo of {num1} and {num2} is {modulus}')


